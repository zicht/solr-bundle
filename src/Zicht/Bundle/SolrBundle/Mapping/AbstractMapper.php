namespace Zicht\Bundle\SolrBundle\Mapping;

abstract class AbstractMapper implements MapperInterface
{
    /** @var string */
    protected $name;

    /**
     * @param string $name
     */
    public function __construct($name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}